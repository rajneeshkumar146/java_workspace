	public static int countHi2(String str) {
		if (str.length() == 0) {
			return 0;
		}

		if (str.length() < 3) {
			if (str.substring(0, 2) == "hi") {
				return 1;
			} else {
				return 0;
			}
		}

		if (str.charAt(0) == 'x') {
			if (str.substring(1, 3) == "hi") {
				return countHi2(str.substring(3));
			} else {
				return countHi2(str.substring(1));
			}

		} else {
			if (str.substring(0, 2) == "hi") {
				return 1 + countHi2(str.substring(2));
			} else {
				return countHi2(str.substring(1));
			}

		}

	}

