package Q001_030;

import java.util.Scanner;

public class Q001_Life_theUnivere_TheEveryThing {
	public static Scanner scn = new Scanner(System.in);

	public static void main(String[] args) {
		solve();
	}

	public static void solve() {

		while(true){
			int n=scn.nextInt();
			
			if(n==42){
				break;
			}
			System.out.println(n);
		}
		
		
	}

}
